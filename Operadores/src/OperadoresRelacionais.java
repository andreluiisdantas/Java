public class OperadoresRelacionais {
    public static void main(String[] args) {
        // Igualdade
        System.out.println(10 == 5 + 5);

        // Diferente de
        System.out.println("A" != "a");

        // Maior que
        System.out.println(20 > 20);

        // Maior ou igual que
        System.out.println(20 >= 20);

        // Menor que
        System.out.println(10 < 10);

        // Menor ou igual que
        System.out.println(10 <= 10);


    }
}
