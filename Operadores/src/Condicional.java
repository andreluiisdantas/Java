public class Condicional {
    public static void main(String[] args) {
        double media = 7.1;
        int faltas = 20;
        boolean postura = true;

        /*
        * Para ser aprovado ele precisa que as três sejam verdaeiras
        * media >= 5
        * faltas < 25
        * postura = true
        *
        * Se falhar na média = recuperação
        *
        * Se falhar nas faltas = sem férias
        *
        * Se falhar na postura = chamar para conversar
        *
        * // Se ele falhar em dois ou mais crtérios ele é reprovado direto
        *
        * */
    }
}
